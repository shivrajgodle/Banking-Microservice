package com.shivraj.accounts;

public class demo {

    /**
     * This method will return the sum of two numbers
     * @param a first number
     * @param b second number
     * @return the sum of two numbers
     */
    public static int add(int a, int b) {
        System.out.println("sum is " + (a+b));
        return a + b;
    }

    /**
     * This method will return the difference of two numbers
     * @param a first number
     * @param b second number
     * @return the difference of two numbers
     */
    public static int subtract(int a, int b) {
        System.out.println("difference is " + (a-b));
        return a - b;
    }

    /**
     * This method will return the product of two numbers
     * @param a first number
     * @param b second number
     * @return the product of two numbers
     */
    public static int multiply(int a, int b) {
        System.out.println("product is " + (a*b));
        return a * b;
    }

    /**
     * This method will return the division of two numbers
     * @param a first number
     * @param b second number
     * @return the division of two numbers
     */
    public static int divide(int a, int b) {
        System.out.println("division is " + (a/b));
        return a / b;
    }

    public static void main(String[] args) {


    }


}
